function capitalizeWords(str) {
    let result = '';
    let capitalizeNext = true; // –§–ª–∞–≥: –Ω—É–∂–Ω–æ –ª–∏ –¥–µ–ª–∞—Ç—å —Å–ª–µ–¥—É—é—â–∏–π —Å–∏–º–≤–æ–ª –∑–∞–≥–ª–∞–≤–Ω—ã–º

    for (let i = 0; i < str.length; i++) {
        const char = str[i];

        if (capitalizeNext && char !== ' ') {
            result += char.toUpperCase();
            capitalizeNext = false;
        } else {
            result += char.toLowerCase();
        }

        // –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π —Å–∏–º–≤–æ–ª ‚Äî –ø—Ä–æ–±–µ–ª, —Å–ª–µ–¥—É—é—â–∏–π –Ω—É–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å –∑–∞–≥–ª–∞–≤–Ω—ã–º
        if (char === ' ') {
            capitalizeNext = true;
        }
    }

    return result;
}

console.log(capitalizeWords("hello world from javascript")); // "Hello World From JavaScript"
